// Ejemplo de operaciones matemáticas en TypeScript

// Suma
const suma: number = 5 + 3;
console.log("Suma:", suma);

// Resta
const resta: number = 10 - 4;
console.log("Resta:", resta);

// Multiplicación
const multiplicacion: number = 6 * 2;
console.log("Multiplicación:", multiplicacion);

// División
const division: number = 20 / 5;
console.log("División:", division);

// Módulo (resto de la división)
const modulo: number = 15 % 4;
console.log("Módulo:", modulo);

// Potencia
const potencia: number = Math.pow(2, 3); // 2 elevado a la 3 potencia
console.log("Potencia:", potencia);
